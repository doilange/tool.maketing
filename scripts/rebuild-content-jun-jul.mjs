function loadEnv() {
  const envPath = path.join(process.cwd(), ".env.local");
  const env = {};
  if (!fs.existsSync(envPath)) return env;
  for (const line of fs.readFileSync(envPath, "utf8").split("\n")) {
    const m = line.match(/^\s*([\w.-]+)\s*=\s*(.*)?\s*$/);
    if (!m) continue;
    let v = m[2] || "";
    if ((v.startsWith('"') && v.endsWith('"')) || (v.startsWith("'") && v.endsWith("'"))) v = v.slice(1, -1);
    env[m[1]] = v.trim();
  }
  return env;
}

function toRow(item, ownerId) {
  return {
    week_group: weekGroup(item.date),
    start_date: item.date,
    due_date: nextDay(item.date), // กำหนดส่ง = วันถัดไปของวันเริ่มงาน
    scheduled_date: item.date,
    day_name: dayName(item.date),
    post_time: "18:00",
    workday_status: workday(item.date),
    platform: "Facebook",
    product: item.product,
    topic: `[${item.product}] ${item.topic}`,
    content_type: item.contentType,
    progress_status: "writing_script",
    post_status: "not_posted",
    approval_status: "pending",
    owner_id: ownerId,
    created_by: ownerId,
    caption: item.caption,
    creative_brief: item.creativeBrief,
    file_url: null,
    note: item.note,
  };
}

async function main() {
  const apply = process.argv.includes("--apply");
  const updateOnly = process.argv.includes("--update-only");
  const mode = updateOnly ? "UPDATE-ONLY (แก้เนื้อหา ไม่ลบ/ไม่สร้างใหม่)" : apply ? "APPLY (เขียนจริง)" : "DRY-RUN";
  console.log(`\n=== Content ${DELETE_FROM}..${DELETE_TO} | ${PLAN.length} โพสต์ | mode: ${mode} ===\n`);

  // พรีวิวตาราง
  for (const item of PLAN) {
    const tag = PROTECT_DATES.has(item.date) ? " [ห้ามแตะ]" : "";
    console.log(`${item.date} ${dayName(item.date)} | ${item.product} | ${item.contentType} | ${item.type} | ${item.topic}${tag}`);
  }

  if (!apply && !updateOnly) {
    console.log(`\n(dry-run) ยังไม่แตะ Supabase — ใช้ --apply (ลบ+ลงใหม่) หรือ --update-only (แก้เนื้อหาเฉย ๆ)\n`);
    return;
  }

  const env = loadEnv();
  if (!env.NEXT_PUBLIC_SUPABASE_URL || !env.SUPABASE_SERVICE_ROLE_KEY) {
    throw new Error("ไม่พบ NEXT_PUBLIC_SUPABASE_URL / SUPABASE_SERVICE_ROLE_KEY ใน .env.local");
  }
  const sb = createClient(env.NEXT_PUBLIC_SUPABASE_URL, env.SUPABASE_SERVICE_ROLE_KEY, {
    auth: { persistSession: false, autoRefreshToken: false },
  });

  // ── โหมดแก้เนื้อหาอย่างเดียว: อัปเดตตาม scheduled_date โดยไม่ลบ/ไม่สร้างใหม่ ข้ามวันที่ห้ามแตะ + ที่โพสต์แล้ว ──
  if (updateOnly) {
    let updated = 0;
    let skipped = 0;
    for (const item of PLAN) {
      if (PROTECT_DATES.has(item.date)) { skipped += 1; continue; }
      const row = toRow(item, null);
      const { data, error } = await sb
        .from("content_tasks")
        .update({
          topic: row.topic,
          product: row.product,
          content_type: row.content_type,
          caption: row.caption,
          creative_brief: row.creative_brief,
          note: row.note,
          due_date: row.due_date,
        })
        .eq("scheduled_date", item.date)
        .neq("post_status", "posted")
        .select("id");
      if (error) throw error;
      updated += data?.length ?? 0;
    }
    console.log(`\nupdate-only: แก้เนื้อหา ${updated} รายการ | ข้าม (ห้ามแตะ) ${skipped} รายการ`);
    console.log(`\n✅ เสร็จสิ้น — เปิดปฏิทินเช็กได้เลย\n`);
    return;
  }

  // owner (โหมด --apply)
  const { data: owner } = await sb.from("profiles").select("id, full_name").eq("full_name", OWNER_NAME).maybeSingle();
  const ownerId = owner?.id ?? null;
  console.log(`\nowner "${OWNER_NAME}": ${ownerId ? "พบ ✓" : "ไม่พบ (owner_id = null)"}`);

  const protectList = [...PROTECT_DATES];

  // 1) ลบงานที่ยังไม่โพสต์ในช่วง (กันวันที่ posted + วันที่ห้ามแตะ)
  let delQuery = sb
    .from("content_tasks")
    .delete()
    .gte("scheduled_date", DELETE_FROM)
    .lte("scheduled_date", DELETE_TO)
    .neq("post_status", "posted");
  if (protectList.length) delQuery = delQuery.not("scheduled_date", "in", `(${protectList.join(",")})`);
  const { data: deleted, error: delErr } = await delQuery.select("id");
  if (delErr) throw delErr;
  console.log(`ลบงานเก่า (ยังไม่โพสต์): ${deleted?.length ?? 0} รายการ`);

  // 2) ลงชุดใหม่ (ข้ามวันที่ห้ามแตะ เพื่อไม่ให้ซ้ำกับของเดิม)
  const rows = PLAN.filter((item) => !PROTECT_DATES.has(item.date)).map((item) => toRow(item, ownerId));
  const { data: inserted, error: insErr } = await sb.from("content_tasks").insert(rows).select("id");
  if (insErr) throw insErr;
  console.log(`ลงคอนเทนต์ใหม่: ${inserted?.length ?? 0} รายการ`);
  console.log(`\n✅ เสร็จสิ้น — เปิดปฏิทินเช็กได้เลย\n`);
}

main().catch((e) => {
  console.error("ERROR:", e.message || e);
  process.exit(1);
});
