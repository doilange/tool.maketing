function parseArgs(argv) {
  const args = {};
  for (let i = 0; i < argv.length; i += 1) {
    const item = argv[i];
    if (!item.startsWith("--")) continue;
    const key = item.slice(2);
    const next = argv[i + 1];
    if (!next || next.startsWith("--")) {
      args[key] = true;
    } else {
      args[key] = next;
      i += 1;
    }
  }
  return args;
}

function parseDate(dateString) {
  const [year, month, day] = dateString.split("-").map(Number);
  if (!year || !month || !day) {
    throw new Error(`Invalid date: ${dateString}. Use YYYY-MM-DD.`);
  }
  return new Date(Date.UTC(year, month - 1, day));
}

function formatDate(date) {
  return date.toISOString().slice(0, 10);
}

function dateRange(startString, endString) {
  const start = parseDate(startString);
  const end = parseDate(endString);
  if (end < start) throw new Error("--end must be on or after --start");

  const dates = [];
  for (let d = new Date(start); d <= end; d.setUTCDate(d.getUTCDate() + 1)) {
    dates.push(new Date(d));
  }
  return dates;
}

function dayName(dateString) {
  return THAI_DAYS[parseDate(dateString).getUTCDay()];
}

function weekGroup(dateString) {
  const d = parseDate(dateString);
  const month = d.getUTCMonth() + 1;
  const day = d.getUTCDate();
  const week = Math.ceil(day / 7);
  return `สัปดาห์ ${week}/${month}`;
}

function withDate(template, dateString) {
  return {
    ...template,
    date: dateString,
    day: dayName(dateString),
    week: weekGroup(dateString),
    workday: ["อาทิตย์", "เสาร์"].includes(dayName(dateString)) ? "dayoff" : "workday",
  };
}

function isStoryTask(task) {
  return task.platform === "Facebook Story" || task.contentType.startsWith("Story");
}

function normalizeProduct(product) {
  return PRODUCT_ALIASES[product] ?? product;
}

function templateProduct(template) {
  if (stripProductPrefix(template.topic) === MONTHLY_INTRO_TASK.topic) {
    return "ประจำเดือน";
  }
  return normalizeProduct(template.product);
}

function templateKey(template) {
  return `${templateProduct(template)}::${stripProductPrefix(template.topic)}`;
}

function stripProductPrefix(topic) {
  return String(topic || "").replace(/^\[[^\]]+\]\s*/, "").trim();
}

function withProductPrefix(topic, product) {
  const normalizedProduct = product === "ประจำเดือน" ? product : normalizeProduct(product);
  const cleanTopic = stripProductPrefix(topic);
  if (!normalizedProduct || normalizedProduct === "-") return cleanTopic;
  return `[${normalizedProduct}] ${cleanTopic}`;
}

function normalizeTask(task) {
  const isMonthlyRoundup =
    stripProductPrefix(task.topic) === MONTHLY_INTRO_TASK.topic;
  const product = isMonthlyRoundup ? "ประจำเดือน" : normalizeProduct(task.product);
  return {
    ...task,
    product,
    topic: withProductPrefix(task.topic, product),
  };
}

function createRotationState() {
  return {
    usedTemplateKeys: new Set(),
    productCounts: new Map(),
    recentProducts: [],
    recentContentTypes: [],
    recentTypes: [],
  };
}

function rememberRotationTask(state, task) {
  const product = templateProduct(task);
  state.productCounts.set(product, (state.productCounts.get(product) ?? 0) + 1);
  state.recentProducts.unshift(product);
  state.recentProducts = state.recentProducts.slice(0, 3);
  state.recentContentTypes.unshift(task.contentType);
  state.recentContentTypes = state.recentContentTypes.slice(0, 2);
  state.recentTypes.unshift(task.type);
  state.recentTypes = state.recentTypes.slice(0, 2);
}

function pickBalancedTemplate(templates, state) {
  let candidates = templates.filter((template) => !state.usedTemplateKeys.has(templateKey(template)));
  if (!candidates.length) {
    state.usedTemplateKeys.clear();
    candidates = templates;
  }

  const scored = candidates.map((template, index) => {
    const product = templateProduct(template);
    const productCount = state.productCounts.get(product) ?? 0;
    const target = MONTHLY_PRODUCT_TARGETS[product] ?? 2;
    const productPosition = PRODUCT_ROTATION.includes(product)
      ? PRODUCT_ROTATION.indexOf(product)
      : PRODUCT_ROTATION.length;
    const recentProductIndex = state.recentProducts.indexOf(product);
    let score = (productCount / target) * 100 + productCount * 8 + productPosition / 10;

    if (recentProductIndex === 0) score += 1000;
    if (recentProductIndex === 1) score += 160;
    if (recentProductIndex === 2) score += 40;
    if (state.recentContentTypes[0] === template.contentType) score += 6;
    if (state.recentTypes[0] === template.type) score += 4;
    if (product === "content อิสระ" && state.recentProducts.includes(product)) score += 60;

    return { template, score: score + index / 1000 };
  });

  scored.sort((a, b) => a.score - b.score);
  const picked = scored[0].template;
  state.usedTemplateKeys.add(templateKey(picked));
  return picked;
}

function buildDefaultPlan(start, end, includeStories) {
  return BASE_PLAN
    .filter((item) => item.date >= start && item.date <= end)
    .filter((item) => includeStories || !isStoryTask(item))
    .map((item) => withDate(item, item.date));
}

function buildAutoPlan(start, end, includeStories) {
  const feedTemplates = [
    ...BASE_PLAN.filter(
      (item) => !item.skipInsert && item.platform === "Facebook" && !item.topic.startsWith("เริ่มต้นเดือนนี้")
    ),
    ...EXTRA_FEED_TEMPLATES,
  ];
  const storyTemplates = BASE_PLAN.filter(
    (item) => !item.skipInsert && item.platform === "Facebook Story"
  );

  const feedState = createRotationState();
  let storyIndex = 0;
  return dateRange(start, end)
    .map((date) => {
      const dow = date.getUTCDay();
      const dateString = formatDate(date);
      const dayOfMonth = date.getUTCDate();
      if (dayOfMonth === 1) {
        const task = withDate(MONTHLY_INTRO_TASK, dateString);
        rememberRotationTask(feedState, task);
        return task;
      }
      if (FEED_DAYS.has(dow)) {
        const task = pickBalancedTemplate(feedTemplates, feedState);
        const datedTask = withDate(task, dateString);
        rememberRotationTask(feedState, datedTask);
        return datedTask;
      }
      if (includeStories && STORY_DAYS.has(dow)) {
        const task = storyTemplates[storyIndex % storyTemplates.length];
        storyIndex += 1;
        return withDate(task, dateString);
      }
      return null;
    })
    .filter(Boolean);
}

function buildTasks({ start, end, auto, includeStories }) {
  const tasks = auto || start !== DEFAULT_START || end !== DEFAULT_END
    ? buildAutoPlan(start, end, includeStories)
    : buildDefaultPlan(start, end, includeStories);

  return tasks.filter((task) => !task.skipInsert).map(normalizeTask);
}

function csvEscape(value) {
  if (value === null || value === undefined) return "";
  const s = String(value);
  if (/[",\n\r]/.test(s)) {
    return `"${s.replace(/"/g, '""')}"`;
  }
  return s;
}

function toCsv(tasks, owner) {
  const rows = tasks.map((task) => [
    task.week,
    task.date,
    task.date,
    task.date,
    task.day,
    task.time,
    task.workday,
    task.platform,
    task.product,
    task.topic,
    task.contentType,
    "เขียนสคริปต์",
    "ยังไม่ได้โพสต์",
    "Pending",
    owner,
    task.caption,
    task.creativeBrief,
    task.note,
    "",
    "",
    "",
  ]);

  return [
    CSV_HEADERS.map(csvEscape).join(","),
    ...rows.map((row) => row.map(csvEscape).join(",")),
  ].join("\r\n");
}

